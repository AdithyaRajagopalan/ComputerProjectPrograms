import java.util.* ;

public class SideOfSquare
{
    public static void main (String [] args )
    {
        // To calculate the side of a square of perimeter 20 cm . 
        int perimeter = 20 ;
        int side = (perimeter / 4); // Side of square = perimeter of square / 4
        System.out.println("Side of a square which has perimeter of 20 cm = " + side);
    }
}