import java.util.* ;

public class AreaOfRectangle
{
    public static void main (String [] args)
    {
        int width = 23 , length = 1 ;
        int area = (width*length) ;
        System.out.println ("Area of rectangle whose length is 1 metre and width is 23 metres = " + area + " metre^2 .");
        
    }
}